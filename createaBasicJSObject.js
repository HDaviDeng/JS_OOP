//Objects in JS are used to model real-world objects, giving them properties and behavior just like their real-world counterparts.
//Here.s an example using these concepts to create a duck object:

let duck = {
    name: "Aflac",
    numLegs: 2
};

//This duck object has two property/value pairs: a name of aflac and a numlegs of 2.


//You'll see how to access the values of those properties. Here's an example:

let duck = {
    name: "Aflac",
    numLegs: 2
};
console.log(duck.name);

//Dot notation is used on the object name, duck, followed by the name of the property, name, to access the value of Aflac.